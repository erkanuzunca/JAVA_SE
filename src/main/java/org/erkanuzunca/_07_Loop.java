package org.erkanuzunca;

public class _07_Loop {

    public static void main(String[] args) {

       for (int i=0;i<10;i++){
           System.out.print(i+" ");
       }
        System.out.println("\n********************");

       int k =0;
       while (k<10){
           System.out.print(k+" ");
           k++;
       }
        System.out.println("\n********************");
       int m=0;
       do{
           System.out.print(m+" ");
           m++;
       }
        while(m<10);



    }

}
